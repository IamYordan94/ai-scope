async function seedTools() {
  console.log('Starting to seed tools...');
  
  for (const tool of initialTools) {
    try {
      const { data, error } = await supabase
        .from('tools')
        .upsert({
          name: tool.name,
          slug: slugify(tool.name),
          description: tool.description,
          category: tool.category,
          website_url: tool.website_url,
          logo_url: tool.logo_url,
          pricing_free: tool.pricing_free,
          pricing_tier: tool.pricing_tier,
          pricing_details: tool.pricing_details,
          features: tool.features,
          use_cases: tool.use_cases,
          tags: tool.tags,
        }, {
          onConflict: 'slug',
        });

      if (error) {
        console.error(`Error seeding ${tool.name}:`, error);
      } else {
        console.log(`âœ“ Seeded ${tool.name}`);
      }
    } catch (error) {
      console.error(`Error seeding ${tool.name}:`, error);
    }
  }
  
  console.log('Seeding complete!');
}

// Run if called directly
seedTools().catch((error) => {
  console.error('Fatal error:', error);
  process.exit(1);
});

